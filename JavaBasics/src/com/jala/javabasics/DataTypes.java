package com.jala.javabasics;

public class DataTypes {
    public static void main(String[] args) {
        // Declaration and initialization of variables
        int intValue = 42;                 // Integer data type
        boolean boolValue = true;          // Boolean data type
        char charValue = 'A';              // Character data type
        float floatValue = 3.14f;          // Float data type (requires 'f' suffix)
        double doubleValue = 42.987654;    // Double data type

        // Printing the values of the variables
        System.out.println("Integer Value: " + intValue);
        System.out.println("Boolean Value: " + boolValue);
        System.out.println("Character Value: " + charValue);
        System.out.println("Float Value: " + floatValue);
        System.out.println("Double Value: " + doubleValue);
    }
}
