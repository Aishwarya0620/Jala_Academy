package operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10, b = 20;

        // Less than
        System.out.println("a < b: " + (a < b));

        // Less than or equal to
        System.out.println("a <= b: " + (a <= b));

        // Greater than
        System.out.println("a > b: " + (a > b));

        // Greater than or equal to
        System.out.println("a >= b: " + (a >= b));
    }
}
